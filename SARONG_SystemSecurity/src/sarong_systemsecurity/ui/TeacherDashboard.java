        private void loadSubjects() {
        try (Connection con = DBConnection.getConnection()) {
            String sql = "SELECT subject_name FROM subjects";
            PreparedStatement pst = con.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                subjects.addItem(rs.getString("subject_name"));
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
        
        private void loadStudentsBySubject() {
        String selectedSubject = subjects.getSelectedItem().toString();
        DefaultTableModel model = new DefaultTableModel(new String[]{"Student Name", "Grade"}, 0);
        jTable1.setModel(model);

        try (Connection con = DBConnection.getConnection()) {
            String sql = "SELECT student_name, grade FROM students WHERE subject_name=?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, selectedSubject);

            ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("student_name"),
                    rs.getString("grade")
                });
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
        
        

