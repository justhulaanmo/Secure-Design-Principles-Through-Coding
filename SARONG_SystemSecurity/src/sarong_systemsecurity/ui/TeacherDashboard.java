    private void btnEDITActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEDITActionPerformed
        int row = jTable1.getSelectedRow();

        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Select a student first!");
            return;
        }

        String studentName = jTable1.getValueAt(row, 0).toString();
        String subjectName = subjects.getSelectedItem().toString();
        String newGrade = JOptionPane.showInputDialog(this, "Enter New Grade:");

        if (newGrade == null) return;

        try (Connection con = DBConnection.getConnection()) {
            String sql = "UPDATE students SET grade=? WHERE student_name=? AND subject_name=?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, newGrade);
            pst.setString(2, studentName);
            pst.setString(3, subjectName);
            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "Grade Updated!");
            loadStudentsBySubject();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnEDITActionPerformed

